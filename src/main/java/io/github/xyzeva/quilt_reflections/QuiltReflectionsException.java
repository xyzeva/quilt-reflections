package io.github.xyzeva.quilt_reflections;

public class QuiltReflectionsException extends RuntimeException {
	public QuiltReflectionsException() {
		super();
	}

	public QuiltReflectionsException(String message) {
		super(message);
	}

	public QuiltReflectionsException(String message, Throwable cause) {
		super(message, cause);
	}

	public QuiltReflectionsException(Throwable cause) {
		super(cause);
	}
}
